/**
  * @brief  Function implementing the ReceiveCanData thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_vReceiveCanData */
void vReceiveCanData(void const * argument)
{
  /* USER CODE BEGIN 5 */

  uint8_t TxMsgCnt=0;

  TxHeader.IDE = CAN_ID_STD;
  TxHeader.StdId = Can_msg_id[0];
  TxHeader.RTR = CAN_RTR_DATA;
  TxHeader.DLC = 8;

  TxData[0] = 0x88;
  TxData[1] = 0x99;
  TxData[2] = 0xAA;
  TxData[3] = 0xBB;
  TxData[4] = 0xCC;
  TxData[5] = 0xDD;
  TxData[6] = 0xEE;
  TxData[7] = 0xFF;

  /* 60 second free-rtos timer start */
  osTimerStart(myTimer01Handle, 60000); //60000=60*1000ms=60sec=1Minute

  /* Infinite loop used to sent CAN messages till timer expired */
  for(;;)
  {
    for(TxMsgCnt=0; (CanMsgStopTx==0 && TxMsgCnt<5); TxMsgCnt++)
    {
      TxHeader.StdId=Can_msg_id[TxMsgCnt];
      HAL_CAN_AddTxMessage(&hcan, &TxHeader, TxData, &TxMailbox);
    }
  }
  /* USER CODE END 5 */
}

